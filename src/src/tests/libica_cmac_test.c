void dump_array(unsigned char *ptr, unsigned int size)
{
	unsigned char *ptr_end;
	unsigned char *h;
	int i = 1, trunc = 0;
	int maxsize = 2000;

	puts("Dump:");

	if (size > maxsize) {
		trunc = size - maxsize;
		size = maxsize;
	}
	h = ptr;
	ptr_end = ptr + size;
	while (h < ptr_end) {
		printf("0x%02x ", *h);
		h++;
		if (i == 16) {
			if (h != ptr_end)
			printf("\n");
			i = 1;
		} else {
			++i;
		}
	}
	printf("\n");
	if (trunc > 0)
	printf("... %d bytes not printed\n", trunc);
}
